import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const recipeRoot = path.join(repoRoot, "resources", "recipes", "v1");
const indexPath = path.join(recipeRoot, "index.json");
const generatedTsPath = path.join(
  repoRoot,
  "src",
  "cs",
  "workbench",
  "services",
  "recipes",
  "common",
  "builtinRecipes.generated.ts",
);
const cliBundlePath = path.join(repoRoot, "cli", "resources", "recipes.v1.json");

const readJson = async (filePath) => JSON.parse(await readFile(filePath, "utf8"));
const index = await readJson(indexPath);

if (index?.schemaVersion !== 1 || !Array.isArray(index.recipes)) {
  throw new Error("resources/recipes/v1/index.json must contain schemaVersion 1 and a recipes array.");
}

const recipes = [];
for (const relativeRecipePath of index.recipes) {
  if (typeof relativeRecipePath !== "string" || !relativeRecipePath.trim()) {
    throw new Error("Recipe index contains an invalid recipe path.");
  }

  const normalizedRecipePath = relativeRecipePath.replaceAll("\\", "/");
  if (normalizedRecipePath.startsWith("/") || normalizedRecipePath.includes("..")) {
    throw new Error(`Recipe path is outside the recipe bundle: ${relativeRecipePath}`);
  }

  const recipe = await readJson(path.join(recipeRoot, normalizedRecipePath));
  recipes.push(recipe);
}

recipes.sort((left, right) => String(left.id ?? "").localeCompare(String(right.id ?? "")));

const bundle = {
  schemaVersion: 1,
  recipes,
};

await mkdir(path.dirname(generatedTsPath), { recursive: true });
await writeFile(
  generatedTsPath,
  `/*---------------------------------------------------------------------------------------------\n` +
  ` * Copyright (c) Conductor Studio. All rights reserved.\n` +
  ` *--------------------------------------------------------------------------------------------*/\n` +
  `\n` +
  `// Generated by scripts/buildRecipeBundle.mjs. Do not edit manually.\n` +
  `\n` +
  `import type { RecipeAuthoring } from "cs/workbench/services/recipes/common/recipe";\n` +
  `\n` +
  `export const builtinRecipes = ${JSON.stringify(recipes, null, 2)} as const satisfies readonly RecipeAuthoring[];\n`,
  "utf8",
);

await mkdir(path.dirname(cliBundlePath), { recursive: true });
await writeFile(cliBundlePath, `${JSON.stringify(bundle, null, 2)}\n`, "utf8");
