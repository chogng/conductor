import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const ruleRoot = path.join(repoRoot, "resources", "templateRules", "v1");
const indexPath = path.join(ruleRoot, "index.json");
const generatedTsPath = path.join(
  repoRoot,
  "src",
  "cs",
  "workbench",
  "services",
  "templateRule",
  "common",
  "builtinTemplateRules.generated.ts",
);
const cliBundlePath = path.join(repoRoot, "cli", "resources", "templateRules.v1.json");

const readJson = async (filePath) => JSON.parse(await readFile(filePath, "utf8"));
const index = await readJson(indexPath);

if (index?.schemaVersion !== 1 || !Array.isArray(index.rules)) {
  throw new Error("resources/templateRules/v1/index.json must contain schemaVersion 1 and a rules array.");
}

const rules = [];
for (const relativeRulePath of index.rules) {
  if (typeof relativeRulePath !== "string" || !relativeRulePath.trim()) {
    throw new Error("Template rule index contains an invalid rule path.");
  }

  const normalizedRelativePath = relativeRulePath.replaceAll("\\", "/");
  if (normalizedRelativePath.startsWith("/") || normalizedRelativePath.includes("..")) {
    throw new Error(`Template rule path is outside the rule bundle: ${relativeRulePath}`);
  }

  const rule = await readJson(path.join(ruleRoot, normalizedRelativePath));
  rules.push(rule);
}

rules.sort((left, right) => String(left.id ?? "").localeCompare(String(right.id ?? "")));

const bundle = {
  schemaVersion: 1,
  rules,
};

await mkdir(path.dirname(generatedTsPath), { recursive: true });
await writeFile(
  generatedTsPath,
  `/*---------------------------------------------------------------------------------------------\n` +
    ` * Copyright (c) Conductor Studio. All rights reserved.\n` +
    ` *--------------------------------------------------------------------------------------------*/\n` +
    `\n` +
    `// Generated by scripts/buildTemplateRuleBundle.mjs. Do not edit manually.\n` +
    `\n` +
    `import type { TemplateDerivationRule } from "src/cs/workbench/services/templateRule/common/templateRule";\n` +
    `\n` +
    `export const builtinTemplateRules = ${JSON.stringify(rules, null, 2)} as const satisfies readonly TemplateDerivationRule[];\n`,
  "utf8",
);

await mkdir(path.dirname(cliBundlePath), { recursive: true });
await writeFile(`${cliBundlePath}`, `${JSON.stringify(bundle, null, 2)}\n`, "utf8");
